    <h1 class="entry-title">Diagnosa Kategori Penyakit Sinusitis</h1>
</div>
<p class="entry-summary">Sinus merupakan rongga kecil yang saling terhubung melalui saluran 
    udara di dalam tulang tengkorak. Rongga kecil ini terletak di bagian belakang tulang dahi (frontal), bagian dalam struktur tulang pipi (maxillary), kedua 
    sisi batang hidung (ethmoidal), dan belakang mata (sphenoidalis).</p>
<p class="entry-content" align="justify">Sinusitis adalah peradangan di lapisan sinus, yang umumnya ditandai dengan pilek, hidung tersumbat, dan nyeri di area wajah. Kondisi ini bisa berlangsung dalam hitungan minggu, bulan, atau bahkan tahun.</p>
<p class="entry-content" align="justify">Sinus menghasilkan lendir yang berfungsi untuk menyaring dan membersihkan bakteri atau partikel lain dalam udara yang dihirup. Sinus juga berfungsi untuk membantu mengendalikan suhu dan kelembapan udara yang dihirup.</p>
<p class="entry-content" align="justify">Sementara itu, sinus merupakan organ yang penting bagi manusia, akan tetapi banyak orang yang tidak memperdulikan kesehatan organ sinus mereka</p>

<img src="./image/gb1.png" width="270px" style="setImageOpacity(3.3);">
<p class="entry-content" align="justify">Sinus juga terbagi menjadi beberapa jenisnya, berikut merupakan jenis penyakit sinusitis dan kurun waktu terjangkit penyakit ini :</p>
<p class="entry-content" align="justify">1. Sinusitis Akut, berlangsung selama 2-4 minggu, dan paling sering terjadi</p>
<p class="entry-content" align="justify">2. Sinusitis Sub-Akut, berlangsung selama 4-12 minggu</p>
<p class="entry-content" align="justify">3. Sinusitis Kronis, berlangsung lebih dari 12 minggu, dan dapat berlanjut hingga berbulan-bulan bahkan bertahun-tahun</p>


<h3 class="entry-title">Berikut beberapa gejala yang dapat dikategorikan terjangkitnya penyakit sinusitis :</h3><p></p>

<p class="entry-content" align="justify">a. Pilek atau flu secara tidak normal (berlebihan)</p>
<img src="./image/flu.jpg" width="250px" style="setImageOpacity(3.3);">

<p class="entry-content" align="justify">b. Hidung terasa tersumbat</p>
<img src="./image/hidung-tersumbat.jpg" width="250px" style="setImageOpacity(3.3);">

<p class="entry-content" align="justify">c. Nyeri dibagian wajah secara menyuluruh</p>
<img src="./image/nyeri-wajah.png" width="250px" style="setImageOpacity(3.3);">


<p class="entry-content" align="justify"></p>

<h3 class="entry-title">Berikut beberapa cara untuk mencegah timbulnya penyakit sinusitis dalam organ sinus kita :</h3>
<p class="entry-content" align="justify">a. Pemakaian masker dengan benar</p>
<p class="entry-content" align="justify">b. Menghindari merokok</p>
<p class="entry-content" align="justify">c. Menghindari paparan zat alergen atau sering disebut debu secara berlebih</p>
<p class="entry-content" align="justify">d. Menghindari kontak dengan orang yang terjangkit penyakit sinusitis , karena dapat menularkan bakteri yang dapat memicu munculnya penyakit sinusitis</p>




<h3 class="entry-title">Hal yang perlu diperhatikan terhadap penyakit sinusitis yang kronis :</h3>
<img src="./image/dokter.jpg" width="250px" style="setImageOpacity(3.3);">
<p class="entry-content" align="justify">Ketika pasien yang telah konsultasi dengan dokter mengenai penyakit sinusitis kronis yang dideritanya tidak berhasil sembuh dengan anjuran beberapa obat
    dari dokter, maka dokter akan menganjurkan tindakan operasi terhadap pasien yang gagal proses penyembuhan dengan obat-obatan saja.
